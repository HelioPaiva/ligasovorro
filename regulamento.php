<strong>7) Premiação </strong> </br></br>
Do total arrecadado R$ 350,00 serão usados para pagar o primeiro colocado da liga do cartola entre a 1º e 35º rodada, onde será pago o valor de R$ 10,00<!--(No site terá uma coluna com o total de vezes que o time ficou em primeiro lugar.)--> </br>

Do valor total arrecadado menos os R$ 350,00, a premiação será dividida da seguinte maneira:</br>
Campeão – 75% da arrecadação Total; </br> 
Vice Campeão – 15% da arrecadação Total ;</br>
3º Colocado – 5% da arrecadação Total;</br>
1º Colocado Liga Cartola – 5% da arrecadação Total;</br></br>

