            1) O campeonato será disputado em confronto direto entre as equipes participantes da liga entre 1º e 35º rodada. Na 36º rodada será disputada as quartas de final, na 37º rodada será disputa as semifinais, e na 38º a Final. Os jogos desta fase serão disputados em jogo único no sistema mata-mata.</br></br>
2) A cada rodada, uma equipe fará um confronto direto com outra equipe, com o objetivo de fazer mais pontos que o adversário, conforme pontuação do Cartola FC. Exemplo:</br>
   Jogos:</br>
Time A x Time B</br>
Time C x Time D.</br>
Caso todos os times já tenham se enfrentado antes da 35º rodada, os times voltarão a se enfrentar conforme o inicio da tabela até 35º rodada.</br></br>
3) Para cada rodada, serão possíveis apenas 3 resultados: vitória, empate ou derrota.</br>
   a) Vitória - 3 pontos para equipe vencedora;</br>
   b) Empate  - 1 ponto para cada equipe;</br>
   c) Derrota - 0 ponto para a equipe derrotada.</br>
   Exemplo de resultados:</br>
   Time A x Time B
   Time A fez 56,36 pontos e Time B fez 36,25 pontos na rodada do Cartola FC.</br>
   No resultado da Liga Sovorro o Time A ganhou 3 pontos e Time B não fez nenhum ponto.</br></br>
4) Nas três últimas rodadas do campeonato brasileiro (36,37 e 38), serão disputadas as quartas de final, semifinais, disputa 3º colocado e final da Liga em jogo único entre os 8 primeiros colocados da fase de pontos corridos (pontuação da Liga Sovorro) com a seguinte ordem:</br>
   a) Quarta de Final 1: 1º Colocado x 8º Colocado;</br>
   b) Quarta de Final 2: 2º Colocado x 7º Colocado;</br>
   c) Quarta de Final 3: 3º Colocado x 6º Colocado;</br>
   d) Quarta de Final 4: 4º Colocado x 5º Colocado;</br>  

   e) Semifinal 1: Vencedor Quarta de Final 1 x Vencedor Quarta de Final 2;</br>
   f) Semifinal 2: Vencedor Quarta de Final 3 x Vencedor Quarta de Final 4;</br>
  
   g) Disputa 3º Colocado: Perdedor Seminifinal 1 x Perdedor Seminifinal 2</br>
   
   h) Final: Vencedor da semifinal 1 x Vencedor da semifinal 2.</br></br>

5) Os critérios de desempate da liga serão:</br> 
   Primeiro - Pontos Feitos "Pontos Pró" (a soma de todos os pontos Cartola FC feitos pela equipe na Liga sovorro). Maior ganha.</br> 
   Segundo  - Pontos levados "Pontos Contra" (a soma de todos os pontos Cartola FC feitos pelos adversários contra a sua equipe). Menos ganha. </br>
   Terceiro - Sorteio.</br></br>
